"""Merge Python + JS coverage into a single MERGED_SUMMARY.md.

Reads:
  tests/coverage/python/coverage.json (coverage.py JSON)
  landing-page/coverage/coverage-summary.json (nyc json-summary)

Emits:
  tests/coverage/MERGED_SUMMARY.md

Intentionally defensive — if either report is missing, the merged
summary still emits a partial section with a clear ⚠ marker so the
reader knows the pipeline was half-run.
"""
from __future__ import annotations

import json
from pathlib import Path

REPO = Path(__file__).resolve().parents[2]
PY_JSON = REPO / "tests" / "coverage" / "python" / "coverage.json"
JS_JSON = REPO / "landing-page" / "coverage" / "coverage-summary.json"
OUT = REPO / "tests" / "coverage" / "MERGED_SUMMARY.md"


def _safe_load(p: Path) -> dict | None:
    if not p.exists():
        return None
    try:
        return json.loads(p.read_text(encoding="utf-8"))
    except Exception:
        return None


def _py_summary(data: dict) -> dict:
    totals = data.get("totals", {})
    return {
        "covered_lines": totals.get("covered_lines", 0),
        "num_statements": totals.get("num_statements", 0),
        "percent_covered": totals.get("percent_covered", 0.0),
        "num_branches": totals.get("num_branches", 0),
        "covered_branches": totals.get("covered_branches", 0),
        "percent_covered_branches": totals.get(
            "percent_covered_branches", 0.0
        ),
        "missing_lines": totals.get("missing_lines", 0),
    }


def _py_per_file(data: dict, top: int = 20) -> list[tuple[str, float, int]]:
    files = data.get("files", {})
    rows = []
    for path, info in files.items():
        s = info.get("summary", {})
        pct = s.get("percent_covered", 0.0)
        missing = s.get("missing_lines", 0)
        num = s.get("num_statements", 0)
        if num < 10:
            continue
        rows.append((path, pct, missing))
    rows.sort(key=lambda r: r[2], reverse=True)
    return rows[:top]


def _js_summary(data: dict) -> dict:
    total = data.get("total", {})
    return {
        "lines_pct": total.get("lines", {}).get("pct", 0.0),
        "lines_total": total.get("lines", {}).get("total", 0),
        "lines_covered": total.get("lines", {}).get("covered", 0),
        "statements_pct": total.get("statements", {}).get("pct", 0.0),
        "branches_pct": total.get("branches", {}).get("pct", 0.0),
        "functions_pct": total.get("functions", {}).get("pct", 0.0),
    }


def main() -> int:
    py = _safe_load(PY_JSON)
    js = _safe_load(JS_JSON)

    lines = ["# MERGED Coverage Summary", ""]

    if py is None:
        lines += ["## Python\n\n⚠ No Python coverage data found at `"
                  f"{PY_JSON.relative_to(REPO)}`.\n"]
    else:
        s = _py_summary(py)
        lines += [
            "## Python (runtime — `coverage.py` JSON)",
            "",
            "| Metric | Value |",
            "|---|---|",
            f"| Line coverage | **{s['percent_covered']:.1f}%** "
            f"({s['covered_lines']}/{s['num_statements']}) |",
            f"| Branch coverage | **{s['percent_covered_branches']:.1f}%** "
            f"({s['covered_branches']}/{s['num_branches']}) |",
            f"| Missing lines | {s['missing_lines']} |",
            "",
            "### Top 20 files by missing-line count",
            "",
            "| File | % | Missing |",
            "|---|---:|---:|",
        ]
        for path, pct, missing in _py_per_file(py):
            lines.append(f"| `{path}` | {pct:.1f}% | {missing} |")
        lines.append("")

    if js is None:
        lines += [
            "## JS\n\n⚠ No JS coverage data found at `"
            f"{JS_JSON.relative_to(REPO)}`.  Run `cd landing-page && "
            "npm run coverage:report` after a Cypress instrumented "
            "run.\n",
        ]
    else:
        s = _js_summary(js)
        lines += [
            "## JS (runtime — `nyc` json-summary)",
            "",
            "| Metric | Value |",
            "|---|---|",
            f"| Line coverage | **{s['lines_pct']:.1f}%** "
            f"({s['lines_covered']}/{s['lines_total']}) |",
            f"| Statement coverage | **{s['statements_pct']:.1f}%** |",
            f"| Branch coverage | **{s['branches_pct']:.1f}%** |",
            f"| Function coverage | **{s['functions_pct']:.1f}%** |",
            "",
        ]

    lines += [
        "---",
        "",
        "Generated by `tests/coverage/merge_and_report.py`. Refresh by",
        "re-running `run_python_inproc.sh` or `run_python.sh` (Python)",
        "and `run_js.sh` (JS), then `python "
        "tests/coverage/merge_and_report.py`.",
        "",
    ]

    OUT.write_text("\n".join(lines), encoding="utf-8")
    print(f"Wrote {OUT.relative_to(REPO)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
